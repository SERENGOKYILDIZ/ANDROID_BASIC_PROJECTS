package com.example.java_collections.Classes;

public class Adres {
    private String Il;
    private String Ilce;

    public Adres() {
    }

    public Adres(String il, String ilce) {
        Il = il;
        Ilce = ilce;
    }

    public String getIl() {
        return Il;
    }

    public void setIl(String il) {
        Il = il;
    }

    public String getIlce() {
        return Ilce;
    }

    public void setIlce(String ilce) {
        Ilce = ilce;
    }
}
