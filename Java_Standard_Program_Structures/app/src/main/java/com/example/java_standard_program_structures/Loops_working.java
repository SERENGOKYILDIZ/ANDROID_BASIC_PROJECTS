package com.example.java_standard_program_structures;

public class Loops_working {
    public static void main(String[] args)
    {
        //For
        for (int i=0;i<4;i++)
            System.out.println("index" + i);

        //While
        int a = 0;
        while (a<4)
        {
            System.out.println("whileIndex" + a);
            a++;
        }
    }
}
